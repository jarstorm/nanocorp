package com.nanocorp.bean;

public class Platforms {

	private Platform facebook;

	private Platform instagram;

	private Platform google;

	public Platform getFacebook() {
		return facebook;
	}

	public void setFacebook(Platform facebook) {
		this.facebook = facebook;
	}

	public Platform getInstagram() {
		return instagram;
	}

	public void setInstagram(Platform instagram) {
		this.instagram = instagram;
	}

	public Platform getGoogle() {
		return google;
	}

	public void setGoogle(Platform google) {
		this.google = google;
	}

}
